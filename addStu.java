    public int StuList(String RegNo) {
        int index = -1;
        for (int i = 0; i < List.size(); i++) {
            if (List.get(i).RegNo.equals(RegNo)) {
                index = i;
                break;
            }
        }
        return index;
    }

    public void del(String reg) {
//        reg = RegNo.getText();
        int index = StuList(reg);
        if (index != -1) {
            List.remove(index);

            JOptionPane.showMessageDialog(null, "Student Deleted!!");
        } else {
            JOptionPane.showMessageDialog(null, "Unable to Find Student");
        }

    }

