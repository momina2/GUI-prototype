    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        FileOutputStream ExcelOut = null;
        BufferedOutputStream out = null;
        XSSFWorkbook tableExporter = null;
        JFileChooser excelFileChooser = new JFileChooser("Desktop");
        excelFileChooser.setDialogTitle("Save As");
        FileNameExtensionFilter fnef = new FileNameExtensionFilter("EXCEL FILES", "xls", "xlsx", "xlsm");
        excelFileChooser.setFileFilter(fnef);
        int choose = excelFileChooser.showSaveDialog(null);
        if (choose == JFileChooser.APPROVE_OPTION) {

            try {
                tableExporter = new XSSFWorkbook();
                XSSFSheet excelSheet = tableExporter.createSheet("Student Data");
                for (int i = 0; i < model.getRowCount(); i++) {
                    XSSFRow row = excelSheet.createRow(i);
                    for (int j = 0; j < model.getColumnCount(); j++) {
                        XSSFCell cell = row.createCell(j);
                        cell.setCellValue(model.getValueAt(i, j).toString());
                    }
                }
                ExcelOut = new FileOutputStream(excelFileChooser.getSelectedFile() + ".xls");
                out = new BufferedOutputStream(ExcelOut);
                tableExporter.write(out);
                JOptionPane.showMessageDialog(null, "Exported to Excel Successfully");
            } catch (FileNotFoundException ex) {
                ex.printStackTrace();
            } catch (IOException ex) {
                ex.printStackTrace();
            } finally {
                try {
                    if (out != null) {
                        out.close();
                    }
                    if (ExcelOut != null) {
                        ExcelOut.close();
                    }

                    if (tableExporter != null) {
                        tableExporter.close();
                    }
                } catch (IOException ex) {
                    ex.printStackTrace();
                }
            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed

